async function updateNewsContent() {
  console.log('🔍 检查数据库中的新闻内容...\n');

  // 获取所有新闻
  const { data: allNews, error } = await supabase
    .from('news')
    .select('id, title, content')
    .order('id', { ascending: true });

  if (error) {
    console.error('❌ 获取新闻失败:', error);
    return;
  }

  console.log(`📊 数据库中共有 ${allNews.length} 篇新闻\n`);

  // 检查内容字段
  let emptyCount = 0;
  let hasContentCount = 0;

  allNews.forEach(news => {
    if (!news.content || news.content.length < 50) {
      emptyCount++;
      console.log(`⚠️  内容为空或过短: ${news.title}`);
    } else {
      hasContentCount++;
    }
  });

  console.log(`\n📋 内容统计:`);
  console.log(`✅ 有内容: ${hasContentCount} 篇`);
  console.log(`⚠️  无内容或过短: ${emptyCount} 篇`);

  if (emptyCount > 0) {
    console.log('\n💡 提示: 请访问 /insert-news 页面，点击"插入所有新闻"按钮来导入完整的文章内容。');
  }
}

