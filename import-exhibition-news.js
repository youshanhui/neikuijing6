async function importExhibitionNews() {
  console.log('开始导入展会信息文章...\n');

  let successCount = 0;
  let failCount = 0;

  for (const news of exhibitionNewsData) {
    try {
      const { error } = await supabase
        .from('news')
        .insert(news);

      if (error) {
        console.log(`❌ 导入失败: ${news.title}`);
        console.log(`   错误: ${error.message}`);
        failCount++;
      } else {
        console.log(`✅ 导入成功: ${news.title}`);
        successCount++;
      }
    } catch (err) {
      console.log(`❌ 导入失败: ${news.title}`);
      console.log(`   错误: ${err.message}`);
      failCount++;
    }

    // 添加延迟以避免API限制
    await new Promise(resolve => setTimeout(resolve, 100));
  }

  console.log('\n========================================');
  console.log(`导入完成！`);
  console.log(`成功: ${successCount} 篇`);
  console.log(`失败: ${failCount} 篇`);
  console.log(`总计: ${exhibitionNewsData.length} 篇`);
  console.log('========================================\n');

  if (successCount > 0) {
    console.log('请刷新网站新闻页面查看新导入的展会信息文章。');
  }
}

importExhibitionNews().catch(console.error);
